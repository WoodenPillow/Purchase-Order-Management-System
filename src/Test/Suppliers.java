    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        if(supname.getText().isEmpty() || supaddress.getText().isEmpty()||
           supcontact.getText().isEmpty() || supemail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Missing Details...!");
        }
        else{
          try {
                con = DBConnection.getConnect();
                pre = con.prepareStatement("Insert into Suppliers (SupId,SupName,SupAddress,SupEmail,SupContact) values (?,?,?,?,?)");
                pre.setString(1,supid.getText());
                pre.setString(2,supname.getText());
                pre.setString(3,supaddress.getText());
                pre.setString(4,supemail.getText());
                pre.setString(5,supcontact.getText());
                pre.executeUpdate();
                JOptionPane.showMessageDialog(this, "Supplier Added SuccessFully.....!");
                clear();
                supAutoId();
                displaySuppliers();
                con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
                }
            
        }
    }//GEN-LAST:event_addbtnActionPerformed

