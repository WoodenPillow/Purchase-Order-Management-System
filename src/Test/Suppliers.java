    private void supplierTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) supplierTable.getModel();
        int myIndex = supplierTable.getSelectedRow();
        rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        supid.setText(model.getValueAt(myIndex, 0).toString());
        supname.setText(model.getValueAt(myIndex, 1).toString());
        supcontact.setText(model.getValueAt(myIndex, 2).toString());
        supemail.setText(model.getValueAt(myIndex, 3).toString());
        supaddress.setText(model.getValueAt(myIndex, 4).toString());
        
        if(supname.getText().isEmpty()){
            rowIndex = 0;
        }
        else{
            rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        }
    }//GEN-LAST:event_supplierTableMouseClicked

