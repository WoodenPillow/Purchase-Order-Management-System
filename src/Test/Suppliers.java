    private void supAutoId(){
        try {
            con = DBConnection.getConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select Max(SupId) from Suppliers");
            rs.next();
            if(rs.getString("MAX(SupId)")==null){
                supid.setText("SP001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(SupId)").substring(2,rs.getString("MAX(SupId)").length()));
                id++;
                supid.setText("SP"+String.format("%03d",id));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
