    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if(itmname.getText().isEmpty() || itmquantity.getText().isEmpty()||
           sellprice.getText().isEmpty() || buyprice.getText().isEmpty() ||
                supplier.getSelectedIndex()==-1 || supplier.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null,"Missing Details...!");
        }
        else{
            try {
                con = DBConnection.getConnect();
                pre = con.prepareStatement("Update Items Set ItName=?,Supplier=?,Category=?,Quantity=?,BuyPrice=?,SellPrice=?,Date=? where ItId=?");
                pre.setString(8,"IM"+String.format("%03d",rowIndex));
                pre.setString(1,itmname.getText());
                pre.setString(2,supplier.getSelectedItem().toString());
                pre.setString(3,category.getSelectedItem().toString());
                pre.setString(4,itmquantity.getText());
                pre.setString(5,buyprice.getText());
                pre.setString(6,sellprice.getText());
                LocalDateTime time = LocalDateTime.now();
                pre.setString(7,time.toString().substring(0,16));
                pre.executeUpdate();
                JOptionPane.showMessageDialog(this, "Items Updated SuccessFully.....!");
                clear();
                itmAutoId();
                displayItems();
                con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

