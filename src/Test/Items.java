    private void itmtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_itmtableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) itmtable.getModel();
        int myIndex = itmtable.getSelectedRow();
        rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        itmid.setText(model.getValueAt(myIndex, 0).toString());
        itmname.setText(model.getValueAt(myIndex, 1).toString());
        int i = 0;
        String sup = model.getValueAt(myIndex, 2).toString();
        String cat = model.getValueAt(myIndex, 3).toString();

        //JOptionPane.showMessageDialog(this, sup);
        itmquantity.setText(model.getValueAt(myIndex, 4).toString());
        buyprice.setText(model.getValueAt(myIndex, 5).toString());
        sellprice.setText(model.getValueAt(myIndex, 6).toString());
        
        if(itmname.getText().isEmpty()){
            rowIndex = 0;
        }
        else{
            rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        }
    }//GEN-LAST:event_itmtableMouseClicked

