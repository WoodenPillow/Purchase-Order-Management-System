    private void itmAutoId(){
        try {
            con = DBConnection.getConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select Max(ItId) from Items");
            rs.next();
            if(rs.getString("MAX(ItId)")==null){
                itmid.setText("IM001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(ItId)").substring(2,rs.getString("MAX(ItId)").length()));
                id++;
                itmid.setText("IM"+String.format("%03d",id));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
    private void getSuppliers(){
        try {
            con = DBConnection.getConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select SupName from Suppliers");
            while(rs.next()){
                String name = rs.getString("SupName");
                supplier.addItem(name);
            }
            supplier.setSelectedIndex(-1);
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
