    private void empAutoId(){
        try {
            con = DBConnection.getConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select Max(CustId) from Customers");
            rs.next();
            if(rs.getString("MAX(CustId)")==null){
                csid.setText("CS001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(CustId)").substring(2,rs.getString("MAX(CustId)").length()));
                id++;
                csid.setText("CS"+String.format("%03d",id));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Customers.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
