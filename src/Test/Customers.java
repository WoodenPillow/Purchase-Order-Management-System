    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        if(csname.getText().isEmpty() || csaddress.getText().isEmpty()||
           cscontact.getText().isEmpty() || csemail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Missing Details...!");
        }
        else{
            try {
                con = DBConnection.getConnect();
                pre = con.prepareStatement("Delete from Customers where CustId=?");
                pre.setString(1,"CS"+String.format("%03d",rowIndex));
                pre.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Deleted SuccessFully.....!");
                clear();
                empAutoId();
                displayEmployees();
                con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if(csname.getText().isEmpty() || csaddress.getText().isEmpty()||
           cscontact.getText().isEmpty() || csemail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Missing Details...!");
        }
        else{
          try {
                con = DBConnection.getConnect();
                pre = con.prepareStatement("Insert into Customers (CustId,CustName,CustContact,CustEmail,CustAddress) values (?,?,?,?,?)");
                pre.setString(1,csid.getText());
                pre.setString(2,csname.getText());
                pre.setString(3,cscontact.getText());
                pre.setString(4,csemail.getText());
                pre.setString(5,csaddress.getText());
                pre.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Added SuccessFully.....!");
                clear();
                empAutoId();
                displayEmployees();
                con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
                }
            
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void customersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customersTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) customersTable.getModel();
        int myIndex = customersTable.getSelectedRow();
        rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        csid.setText(model.getValueAt(myIndex, 0).toString());
        csname.setText(model.getValueAt(myIndex, 1).toString());
        cscontact.setText(model.getValueAt(myIndex, 2).toString());
        csemail.setText(model.getValueAt(myIndex, 3).toString());
        csaddress.setText(model.getValueAt(myIndex, 4).toString());
        
        if(csname.getText().isEmpty()){
            rowIndex = 0;
        }
        else{
            rowIndex = Integer.parseInt(model.getValueAt(myIndex, 0).toString().substring(2));
        }
    }//GEN-LAST:event_customersTableMouseClicked

