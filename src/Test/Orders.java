    private void addToBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToBillActionPerformed
        // TODO add your handling code here:
        if(ordertable.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Empty List.....!");
            return;
        }
        try {
                con = DBConnection.getConnect();
                pre = con.prepareStatement("Insert into Orders (OrId,ItId,ItName,CustomerId,CustomerName,Quantity,TotalPrice,OrDate) values(?,?,?,?,?,?,?,?)");
                pre.setString(1,orderid.getText());
                pre.setString(2,sbItemsId.toString());
                pre.setString(3,sbItemsName.toString());
                pre.setString(4,customerId);
                pre.setString(5,customerName);
                pre.setString(6,String.valueOf(countItems));
                pre.setString(7,String.valueOf(grandTotal));
                LocalDateTime now = LocalDateTime.now();
                pre.setString(8,now.toString().substring(0,16));
                pre.executeUpdate();
                JOptionPane.showMessageDialog(this, "Bill Added SuccessFully.....!");
                orderAutoId();
                customerId = "";
                customerName = "";
                countItems = 0;
                grandTotal = 0;
                price = 0;
                itmId = "";
                itmName = "";
                custid.setText("");
                custname.setText("");
                itemId.setText("");
                itemName.setText("");
                sbItemsId.delete(0, sbItemsId.length());
                sbItemsName.delete(0, sbItemsName.length());
                totalcost.setText("Price");
                DefaultTableModel model = (DefaultTableModel) ordertable.getModel();
                while(model.getRowCount()>0){
                    model.removeRow(0);
                }
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_addToBillActionPerformed

