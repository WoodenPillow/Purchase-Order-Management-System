    private void orderAutoId(){
        try {
            con = DBConnection.getConnect();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select Max(OrId) from Orders");
            rs.next();
            if(rs.getString("MAX(OrId)")==null){
                orderid.setText("OR001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(OrId)").substring(2,rs.getString("MAX(OrId)").length()));
                id++;
                orderid.setText("OR"+String.format("%03d",id));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Orders.class.getName()).log(Level.SEVERE, null, ex);
        }
    } 
