    private void addToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartActionPerformed
        // TODO add your handling code here:
        if(custid.getText().equals("") ||custname.getText().equals("") || 
           itemId.getText().equals("") ||itemName.getText().equals("") ||
           quantity.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Details");
        }
        else{
            int Total = Integer.parseInt(quantity.getText())*price;
            grandTotal+=Total;
            DefaultTableModel model = (DefaultTableModel) ordertable.getModel();
            int nextRowId = model.getRowCount();
            model.addRow(new Object[]{
            nextRowId+1,
            itmName,
            price,
            quantity.getText(),
            Total
        });
        }
        countItems+=Integer.parseInt(quantity.getText());
        sbItemsId.append(itmId).append("-");
        sbItemsName.append(itmName).append("-");
        quantity.setText("");
        itemId.setText("");
        itemName.setText("");
        totalcost.setText(String.valueOf(grandTotal));
    }//GEN-LAST:event_addToCartActionPerformed

